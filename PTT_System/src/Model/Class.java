package Model;

public class Class {
	
	private int classID;
	private int numOfStudent;
	
	public Class(int classID, int numOfStudent) {
		
		this.classID = classID;
		this.numOfStudent = numOfStudent;
		
	}

	public int getClassID() {
		return classID;
	}

	public int getNumOfStudent() {
		return numOfStudent;
	}
	
	
}
